$(document).ready(function () {

	console.log($(".st96"))
	var pathArray = $(".st96")

	$.each(pathArray, function (i, d) {


		var pathLength = d.getTotalLength()
		console.log(pathLength)

		$(d).css("stroke-dasharray" , pathLength + " " + pathLength)
		$(d).css("stroke-dashoffset" , pathLength)
	})
})

